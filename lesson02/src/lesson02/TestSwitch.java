package lesson02;

public class TestSwitch {
public static void main(String[] args) {
	int options = 2;
	switch(options) {
	case 1:
		System.out.println("selected 1");
		break;
	case 2:
		System.out.println("selected 2");
		break;
	case 3:
		System.out.println("selected 3");
		break;
	default:
		System.out.println("no selected");
		break;
	}
}
}
